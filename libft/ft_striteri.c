#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;
	int	len;

	i = 0;
	len = ft_strlen(s);
	if (!s || !(*f))
		return ;
	while (i++ < len)
		(*f)(i - 1, &s[i - 1]);
}
